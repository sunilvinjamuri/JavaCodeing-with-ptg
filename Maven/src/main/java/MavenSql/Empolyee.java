package MavenSql;

public class Empolyee {

	
		
		int id;
		String name;
		String add;
		String zip;
		public Empolyee() {
			super();
			this.id = id;
			this.name = name;
			this.add = add;
			this.zip = zip;
		}
		public int getId() {
			return id;
		}
		
		public String getName() {
			return name;
		}
		
		public String getAdd() {
			return add;
		}
		
		public String getZip() {
			return zip;
		}
		public Empolyee(int id, String name, String add, String zip) {
			super();
			this.id = id;
			this.name = name;
			this.add = add;
			this.zip = zip;
		}
		
		  


	}


